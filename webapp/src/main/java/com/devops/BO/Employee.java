package com.devops.BO;

public class Employee {
	
	private String emp_name;
	private Integer emp_id;
	private String emp_location;
	private String emp_passwd;
	public String getEmp_passwd() {
		return emp_passwd;
	}
	public void setEmp_passwd(String emp_passwd) {
		this.emp_passwd = emp_passwd;
	}
	public String getEmp_name() {
		return emp_name;
	}
	public void setEmp_name(String emp_name) {
		this.emp_name = emp_name;
	}
	public Integer getEmp_id() {
		return emp_id;
	}
	public void setEmp_id(Integer emp_id) {
		this.emp_id = emp_id;
	}
	public String getEmp_location() {
		return emp_location;
	}
	public void setEmp_location(String emp_location) {
		this.emp_location = emp_location;
	}

}
